using System.Text;
using XamppMultidomainManager.Models;

namespace XamppMultidomainManager.Services;

public class VirtualHostService
{
    private readonly DatabaseService _dbService;
    private readonly XamppService _xamppService;
    private readonly SslService _sslService;

    private string XamppPath => _xamppService.XamppPath;

    public VirtualHostService(DatabaseService dbService, XamppService xamppService, SslService sslService)
    {
        _dbService = dbService;
        _xamppService = xamppService;
        _sslService = sslService;
    }

    public async Task<(bool Valid, string Message)> RewriteConfig()
    {
        var hosts = _dbService.GetAllHosts().Where(h => h.Enabled).ToList();
        var vhostsPath = Path.Combine(XamppPath, "apache", "conf", "extra", "httpd-vhosts.conf");
        var sslConf = Path.Combine(XamppPath, "apache", "conf", "extra", "httpd-ssl.conf");

        await EnsureVhostIncludeEnabled();
        await EnsureSslEnabled();
        await BlockXamppAliases();

        var httpSB = new StringBuilder();
        httpSB.AppendLine("# Virtual Hosts");
        httpSB.AppendLine("# Auto-generated by XROW.ASIA / XamppMultidomainManager at " + DateTime.Now.ToString("yyyy-MM-dd HH:mm:ss"));
        httpSB.AppendLine();
        httpSB.AppendLine("# XROW.ASIA :: XamppMultidomainManager managed HTTP vhosts - do not edit manually");
        httpSB.AppendLine();

        var sslSB = new StringBuilder();

        foreach (var host in hosts)
        {
            var hasSsl = _sslService.CertificateExists(host.DomainName);
            var root = host.DocumentRoot.Replace('\\', '/');

            EnsureDirectoryExists(root);

            httpSB.AppendLine($"# {host.DomainName}");
            WriteVhostBlock(httpSB, host.DomainName, $"admin@{host.DomainName}", root, host.ServerAlias, false);

            if (hasSsl)
            {
                sslSB.AppendLine($"# {host.DomainName}");
                WriteVhostBlock(sslSB, host.DomainName, $"admin@{host.DomainName}", root, host.ServerAlias, true,
                    _sslService.CertDir.Replace('\\', '/'), _sslService.KeyDir.Replace('\\', '/'));
                sslSB.AppendLine();
            }
        }

        httpSB.AppendLine("# Fallback: localhost serves XAMPP default htdocs");
        WriteVhostBlock(httpSB, "localhost", "admin@localhost",
            Path.Combine(XamppPath, "htdocs").Replace('\\', '/'), null, false);

        try
        {
            var dir = Path.GetDirectoryName(vhostsPath);
            if (!string.IsNullOrEmpty(dir) && !Directory.Exists(dir))
                Directory.CreateDirectory(dir);
            await File.WriteAllTextAsync(vhostsPath, httpSB.ToString(), Encoding.UTF8);
        }
        catch (Exception ex)
        {
            System.Diagnostics.Debug.WriteLine($"Failed to write http vhosts config: {ex.Message}");
            return (false, $"Failed to write HTTP vhosts: {ex.Message}");
        }

        try
        {
            await WriteSslVhosts(sslConf, sslSB);
        }
        catch (Exception ex)
        {
            System.Diagnostics.Debug.WriteLine($"Failed to write ssl vhosts config: {ex.Message}");
            return (false, $"Failed to write SSL vhosts: {ex.Message}");
        }

        // Validate config before returning
        var (valid, msg) = _xamppService.TestConfig();
        return (valid, valid ? "Configuration written successfully" : msg);
    }

